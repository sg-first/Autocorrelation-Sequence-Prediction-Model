from keras import Input,layers
from keras.models import Model
data_input=Input(shape=(1,))
x=layers.Dense(16,activation="sigmoid")(data_input)
x=layers.Dropout(0.07)(x)
y=layers.Dense(32,activation="sigmoid")(x)
y=layers.normalization.BatchNormalization()(y)
y=layers.Dense(64,activation="sigmoid")(y)
y=layers.normalization.BatchNormalization()(y)
y=layers.Dense(16,activation="sigmoid")(y)
y=layers.Dropout(0.07)(y)
y=layers.add([x,y])
y=layers.Dense(128,activation="sigmoid")(y)
predict_3=layers.Dense(1)(x)
model_3=Model(data_input,predict_3)
